@app.route("/predict", methods=["POST"])
def predict_image():
    """Handle image upload, run detection, redirect to result page."""
    if "image" not in request.files:
        return jsonify({"error": "No file uploaded"}), 400

    file = request.files["image"]
    if file.filename == "" or not _allowed(file.filename):
        return jsonify({"error": "Invalid file type"}), 400

    ext = file.filename.rsplit(".", 1)[1].lower()
    unique_name = f"{uuid.uuid4().hex}.{ext}"
    save_path = os.path.join(UPLOAD_DIR, unique_name)
    file.save(save_path)

    batch, _ = load_and_preprocess(save_path)
    if batch is None:
        return jsonify({"error": "Could not process image"}), 400

    result = predict(batch, image_path=save_path)

    # Collect geolocation if provided by the browser
    location = {}
    lat = request.form.get("latitude")
    lng = request.form.get("longitude")
    if lat and lng:
        try:
            location = {
                "lat": float(lat),
                "lng": float(lng),
                "address": request.form.get("address", "Unknown"),
            }
        except (ValueError, TypeError):
            pass

    record = {
        "id": uuid.uuid4().hex[:12],
        "timestamp": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        "filename": secure_filename(file.filename),
        "image": unique_name,
        "source": "upload",
        "location": location,
        **result,
    }
    _add_record(record)

    return jsonify({"redirect": url_for("result_page", record_id=record["id"]), **record})


