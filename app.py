@app.route("/api/stats")
def api_stats():
    records = _load_history()
    total = len(records)
    fires = sum(1 for r in records if r.get("detailed_label") == "Fire")
    smoke = sum(1 for r in records if r.get("detailed_label") == "Smoke")
    safe = total - fires - smoke
    avg_conf = round(sum(r["confidence"] for r in records) / total, 1) if total else 0
    risk_dist = {}
    for r in records:
        risk_dist[r["risk_level"]] = risk_dist.get(r["risk_level"], 0) + 1

    # Timeline data — group by date
    timeline = {}
    for r in records:
        date = r["timestamp"][:10]  # YYYY-MM-DD
        if date not in timeline:
            timeline[date] = {"fire": 0, "smoke": 0, "safe": 0}
        dl = r.get("detailed_label", "")
        if dl == "Fire":
            timeline[date]["fire"] += 1
        elif dl == "Smoke":
            timeline[date]["smoke"] += 1
        else:
            timeline[date]["safe"] += 1

    return jsonify({
        "total": total,
        "fires": fires,
        "smoke": smoke,
        "safe": safe,
        "avg_confidence": avg_conf,
        "risk_distribution": risk_dist,
        "timeline": timeline,
        "accuracy": {"fire": 96.0, "smoke": 74.0, "no_fire": 90.0, "overall": 86.7},
    })


