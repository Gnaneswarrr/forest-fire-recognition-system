def _camera_loop():
    global _cam_active
    cap = cv2.VideoCapture(0)
    if not cap.isOpened():
        _cam_active = False
        return
    while _cam_active:
        ret, _ = cap.read()
        if not ret:
            break
        time.sleep(0.03)
    cap.release()


@app.route("/camera/start", methods=["POST"])
def camera_start():
    global _cam_active, _cam_thread
    with _cam_lock:
        if _cam_active:
            return jsonify({"status": "already_running"})
        _cam_active = True
        _cam_thread = threading.Thread(target=_camera_loop, daemon=True)
        _cam_thread.start()
    return jsonify({"status": "started"})


@app.route("/camera/stop", methods=["POST"])
def camera_stop():
    global _cam_active
    _cam_active = False
    return jsonify({"status": "stopped"})


def _gen_frames():
    cap = cv2.VideoCapture(0)
    while _cam_active and cap.isOpened():
        ok, frame = cap.read()
        if not ok:
            break
        _, buf = cv2.imencode(".jpg", frame)
        yield (b"--frame\r\n"
               b"Content-Type: image/jpeg\r\n\r\n" + buf.tobytes() + b"\r\n")
        time.sleep(0.03)
    cap.release()


@app.route("/camera/feed")
def camera_feed():
    return Response(_gen_frames(), mimetype="multipart/x-mixed-replace; boundary=frame")


@app.route("/camera/capture", methods=["POST"])
def camera_capture():
    cap = cv2.VideoCapture(0)
    ok, frame = cap.read()
    cap.release()
    if not ok:
        return jsonify({"error": "Camera capture failed"}), 500

    fname = f"cap_{uuid.uuid4().hex[:8]}.jpg"
    path = os.path.join(UPLOAD_DIR, fname)
    cv2.imwrite(path, frame)

    batch, _ = preprocess_frame(frame)
    if batch is None:
        return jsonify({"error": "Preprocessing failed"}), 500

    result = predict(batch, image_path=path)

    # Collect geolocation from JSON body or form
    location = {}
    data = request.get_json(silent=True) or {}
    lat = data.get("latitude") or request.form.get("latitude")
    lng = data.get("longitude") or request.form.get("longitude")
    if lat and lng:
        try:
            location = {
                "lat": float(lat),
                "lng": float(lng),
                "address": data.get("address") or request.form.get("address", "Unknown"),
            }
        except (ValueError, TypeError):
            pass

    record = {
        "id": uuid.uuid4().hex[:12],
        "timestamp": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        "filename": fname,
        "image": fname,
        "source": "camera",
        "location": location,
        **result,
    }
    _add_record(record)
    return jsonify(record)


