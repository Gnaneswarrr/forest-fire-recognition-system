@app.route("/api/send-alert", methods=["POST"])
def api_send_alert():
    """Send a real email alert via SMTP."""
    data = request.get_json(silent=True) or {}
    email = data.get("email", "").strip()
    smtp_host = data.get("smtp_host", "smtp.gmail.com").strip()
    smtp_port = int(data.get("smtp_port", 587))
    smtp_user = data.get("smtp_user", "").strip()
    smtp_pass = data.get("smtp_pass", "")
    is_test = data.get("test", False)

    if not email:
        return jsonify({"error": "Recipient email is required"}), 400
    if not smtp_user or not smtp_pass:
        return jsonify({"error": "SMTP username and password are required"}), 400
    # Basic validation
    if "@" not in email or "@" not in smtp_user:
        return jsonify({"error": "Invalid email format"}), 400
    # Restrict to known SMTP ports
    if smtp_port not in (25, 465, 587, 2525):
        return jsonify({"error": "Invalid SMTP port"}), 400

    if is_test:
        subject = "Forest Fire Recognition — Test Alert"
        body = (
            "<h2>🔥 Test Alert — Forest Fire Recognition System</h2>"
            "<p>This is a test email to confirm your alert configuration is working correctly.</p>"
            "<p>When a real fire or smoke is detected, you will receive an alert with:</p>"
            "<ul>"
            "<li>Detection type (Fire / Smoke)</li>"
            "<li>Confidence level</li>"
            "<li>Risk assessment</li>"
            "<li>Location coordinates (if available)</li>"
            "<li>Recommended actions</li>"
            "</ul>"
            f"<p><small>Sent at {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}</small></p>"
        )
    else:
        det = data.get("detection", {})
        subject = f"🔥 FIRE ALERT — {det.get('label', 'Detection')} — {det.get('confidence', 0):.0f}% Confidence"
        loc_info = ""
        loc = det.get("location", {})
        if loc.get("lat"):
            loc_info = f"<p><strong>Location:</strong> {loc.get('address', 'Unknown')} ({loc['lat']}, {loc['lng']})</p>"
        body = (
            f"<h2>🔥 Forest Fire Alert</h2>"
            f"<p><strong>Detection:</strong> {det.get('label', 'N/A')}</p>"
            f"<p><strong>Confidence:</strong> {det.get('confidence', 0):.1f}%</p>"
            f"<p><strong>Risk Level:</strong> {det.get('risk_level', 'N/A')}</p>"
            f"{loc_info}"
            f"<p><strong>Recommendation:</strong> {det.get('recommendation', 'N/A')}</p>"
            f"<p><small>Detected at {det.get('timestamp', datetime.now().strftime('%Y-%m-%d %H:%M:%S'))}</small></p>"
        )

    try:
        msg = MIMEMultipart("alternative")
        msg["Subject"] = subject
        msg["From"] = smtp_user
        msg["To"] = email
        msg.attach(MIMEText(body, "html"))

        if smtp_port == 465:
            server = smtplib.SMTP_SSL(smtp_host, smtp_port, timeout=10)
        else:
            server = smtplib.SMTP(smtp_host, smtp_port, timeout=10)
            server.starttls()
        server.login(smtp_user, smtp_pass)
        server.sendmail(smtp_user, [email], msg.as_string())
        server.quit()
        return jsonify({"status": "sent", "message": f"Email sent to {email}"})
    except smtplib.SMTPAuthenticationError:
        return jsonify({"error": "SMTP authentication failed. Check username/password."}), 401
    except smtplib.SMTPException as e:
        return jsonify({"error": f"SMTP error: {str(e)}"}), 500
    except Exception as e:
        return jsonify({"error": f"Failed to send email: {str(e)}"}), 500


