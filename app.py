@app.route("/api/weather")
def api_weather():
    """Fetch current weather + calculate Fire Weather Index."""
    city = request.args.get("city", "")
    lat = request.args.get("lat", "")
    lon = request.args.get("lon", "")

    location_name = city or "Unknown"

    # Step 1: Geocode city name to lat/lon using Open-Meteo geocoding
    if not (lat and lon):
        if not city:
            return jsonify({"error": "Provide city or lat/lon"}), 400
        geo_url = f"https://geocoding-api.open-meteo.com/v1/search?name={urllib.parse.quote(city)}&count=1"
        try:
            req = urllib.request.Request(geo_url, headers={"User-Agent": "ForestFireRecognition/1.0"})
            with urllib.request.urlopen(req, timeout=10) as resp:
                geo = json.loads(resp.read().decode("utf-8"))
            results = geo.get("results", [])
            if not results:
                return jsonify({"error": f"City '{city}' not found"}), 404
            lat = results[0]["latitude"]
            lon = results[0]["longitude"]
            location_name = f"{results[0].get('name', city)}, {results[0].get('country_code', '')}"
        except Exception as e:
            return jsonify({"error": f"Geocoding error: {str(e)}"}), 502

    # Step 2: Fetch weather from Open-Meteo
    wx_url = (
        f"https://api.open-meteo.com/v1/forecast?"
        f"latitude={lat}&longitude={lon}"
        f"&current=temperature_2m,relative_humidity_2m,wind_speed_10m,"
        f"precipitation,cloud_cover,surface_pressure,weather_code"
        f"&timezone=auto"
    )
    try:
        req = urllib.request.Request(wx_url, headers={"User-Agent": "ForestFireRecognition/1.0"})
        with urllib.request.urlopen(req, timeout=10) as resp:
            data = json.loads(resp.read().decode("utf-8"))
    except Exception as e:
        return jsonify({"error": f"Weather API error: {str(e)}"}), 502

    cur = data.get("current", {})
    temp_c = cur.get("temperature_2m", 20)
    humidity = cur.get("relative_humidity_2m", 50)
    wind_kmh = cur.get("wind_speed_10m", 0)
    rain_mm = cur.get("precipitation", 0)
    clouds = cur.get("cloud_cover", 0)
    pressure = cur.get("surface_pressure", 1013)
    wmo_code = cur.get("weather_code", 0)

    # WMO weather code to description
    WMO_DESC = {
        0: "Clear Sky", 1: "Mainly Clear", 2: "Partly Cloudy", 3: "Overcast",
        45: "Foggy", 48: "Rime Fog", 51: "Light Drizzle", 53: "Moderate Drizzle",
        55: "Dense Drizzle", 61: "Slight Rain", 63: "Moderate Rain", 65: "Heavy Rain",
        71: "Slight Snow", 73: "Moderate Snow", 75: "Heavy Snow", 80: "Slight Showers",
        81: "Moderate Showers", 82: "Violent Showers", 95: "Thunderstorm",
        96: "Thunderstorm + Hail", 99: "Severe Thunderstorm",
    }
    weather_desc = WMO_DESC.get(wmo_code, "Unknown")

    # --- Calculate simplified Fire Weather Index (0-100) ---
    f_temp = min(max((temp_c - 5) / 40 * 100, 0), 100)
    f_hum = min(max((100 - humidity) / 80 * 100, 0), 100)
    f_wind = min(max(wind_kmh / 60 * 100, 0), 100)
    f_rain = max(100 - rain_mm * 20, 0)

    fwi_score = int(f_temp * 0.35 + f_hum * 0.30 + f_wind * 0.20 + f_rain * 0.15)
    fwi_score = min(max(fwi_score, 0), 100)

    if fwi_score <= 20:
        fwi_level, fwi_desc = "Low", "Fire risk is minimal. Conditions are moist and cool."
        fwi_rec = "Normal activities permitted. Maintain standard fire safety awareness."
    elif fwi_score <= 40:
        fwi_level, fwi_desc = "Moderate", "Some fire risk. Dry patches may exist."
        fwi_rec = "Be cautious with open flames. Monitor local conditions closely."
    elif fwi_score <= 60:
        fwi_level, fwi_desc = "High", "Significant fire risk. Vegetation is drying out."
        fwi_rec = "Avoid open burning. Keep firefighting equipment ready. Alert forest rangers."
    elif fwi_score <= 80:
        fwi_level, fwi_desc = "Very High", "Severe fire risk. Fires can start and spread rapidly."
        fwi_rec = "No open burning. Prepare evacuation plans. Alert emergency services."
    else:
        fwi_level, fwi_desc = "Extreme", "Critical fire danger. Any spark can cause catastrophic fire."
        fwi_rec = "Immediate high alert. Evacuate fire-prone areas. Full emergency readiness."

    return jsonify({
        "location": location_name,
        "updated": cur.get("time", datetime.utcnow().strftime("%Y-%m-%d %H:%M")),
        "weather": {
            "temp_c": temp_c,
            "humidity": humidity,
            "wind_kmh": wind_kmh,
            "visibility_m": 10000,
            "clouds": clouds,
            "pressure": pressure,
            "description": weather_desc,
            "rain_1h": rain_mm,
        },
        "fwi": {
            "score": fwi_score,
            "level": fwi_level,
            "description": fwi_desc,
            "recommendation": fwi_rec,
            "factors": {
                "temperature": round(f_temp, 1),
                "humidity": round(f_hum, 1),
                "wind": round(f_wind, 1),
                "precipitation": round(f_rain, 1),
            },
        },
    })


