def _allowed(filename: str) -> bool:
    return "." in filename and filename.rsplit(".", 1)[1].lower() in ALLOWED_EXTENSIONS


def _generate_report(record: dict) -> str:
    sep = "=" * 56
    loc = record.get("location", {})
    location_lines = []
    if loc.get("lat"):
        location_lines = [
            f"  Latitude        : {loc['lat']}",
            f"  Longitude       : {loc['lng']}",
            f"  Address         : {loc.get('address', 'Unknown')}",
        ]
    lines = [
        sep,
        "  FOREST FIRE RECOGNITION — ANALYSIS REPORT",
        sep,
        f"  Detection ID   : {record['id']}",
        f"  Date / Time    : {record['timestamp']}",
        f"  Source File     : {record.get('filename', 'N/A')}",
        sep,
        f"  Prediction      : {record['label']}",
        f"  Detail Class    : {record['detailed_label']}",
        f"  Confidence      : {record['confidence']:.1f}%",
        f"  Risk Level      : {record['risk_level']}",
        sep,
        *location_lines,
        *([ sep ] if location_lines else []),
        "  Class Probabilities:",
        f"    Fire   : {record['probabilities']['fire']:.1f}%",
        f"    Smoke  : {record['probabilities']['smoke']:.1f}%",
        f"    No Fire: {record['probabilities']['no_fire']:.1f}%",
        sep,
        f"  Recommendation  : {record['recommendation']}",
        sep,
        "  Report generated by Forest Fire Recognition System",
        sep,
    ]
    return "\n".join(lines)


